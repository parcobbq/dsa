			<p>The Center was initially conceived by Evelyn Lowery, the wife of Joseph Lowery, and Juanita Abernathy, the widow of Ralph David Abernathy, along with former Atlanta mayor Andrew Young and longtime House Representative John Lewis, all of whom were part of the movement to grant civil rights to African-Americans during the 1960s.The Lowerys met with Mayor Shirley Franklin in 2001, who was warm to the concept of a museum honoring Atlanta's civil rights history but, due to more pressing issues with the city's finances, was unable to offer much more than that at the time. The group met again in 2005, at which point Franklin signed onto the project, and the Center for Civil and Human Rights was established in 2007, along with its initial fundraising efforts. Five architectural firms presented their proposals in 2009,[1] with The Center ultimately selecting a design by architect Philip Freelon for a 90,000-square-foot (8,400 m2) museum that would break ground in 2010 and open in 2012. The 2.5-acre (1.0 ha) site for the museum, at Pemberton Place, was donated by The Coca-Cola Company and placed the museum adjacent to three popular tourist attractions, the Georgia Aquarium, the World of Coca-Cola and Centennial Olympic Park. </p>
